// Helper function to extract curriculum data
function getCurriculumDetails(input: GenerateObjectivesInput) {
  const annualPrograms = curriculumData.Curriculum_subject.Annual_Programs as any;
  const yearData = annualPrograms[input.studyYear];

  if (!yearData) {
    throw new Error(`Study year "${input.studyYear}" not found in curriculum.`);
  }

  const fieldData = yearData.Learning_Field.find(
    (field: any) => field.Field_Title === input.learningField
  );

  if (!fieldData) {
    throw new Error(`Learning field "${input.learningField}" not found for year "${input.studyYear}".`);
  }

  const finalCompetencyForField = fieldData.Final_Competency;

  // Verify knowledgeResource and specificResource exist, even if not directly used in the prompt in this form
  // to ensure input validity. The prompt will use them as raw strings.
  const knowledgeResourceCategory = fieldData.Knowledge_resources[input.knowledgeResource];
  if (!knowledgeResourceCategory) {
      throw new Error(`Knowledge resource category "${input.knowledgeResource}" not found for field "${input.learningField}".`);
  }
  if (!knowledgeResourceCategory.includes(input.specificResource)) {
      throw new Error(`Specific resource "${input.specificResource}" not found in category "${input.knowledgeResource}".`);
  }

  return {
    finalCompetencyForField,
  };
}

