const generateObjectivesFlow = ai.defineFlow(
  {
    name: 'generateObjectivesFlow',
    inputSchema: GenerateObjectivesInputSchema,
    outputSchema: GenerateObjectivesOutputSchema,
  },
  async (input) => {
    const { finalCompetencyForField } = getCurriculumDetails(input);

    const promptInput = {
      ...input,
      finalCompetencyForField: finalCompetencyForField,
    };

    const { output } = await generateObjectivesPrompt(promptInput);

    if (!output) {
      throw new Error('Failed to generate objectives.');
    }

    return {
      objectives: output.objectives,
      terminalCompetence: finalCompetencyForField,
    };
  }
);

export async function generateObjectives(
  input: GenerateObjectivesInput
): Promise<GenerateObjectivesOutput> {
  return generateObjectivesFlow(input);
}
